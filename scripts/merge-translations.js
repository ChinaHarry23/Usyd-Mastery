#!/usr/bin/env node
/**
 * Merges per-chapter JSON translation files into the per-course
 * section-translations.js file.
 *
 * Usage:
 *   node scripts/merge-translations.js <courseDir>
 *
 *   <courseDir>  e.g. 5046NLP, 9001Py, 9123DataStructureAlgo
 *
 * Reads:
 *   <courseDir>/shared/translations-data/chapterN.json
 * Writes:
 *   <courseDir>/shared/section-translations.js
 *
 * The file globalVarName is auto-derived (COMP5046_SECTION_TRANSLATIONS for 5046NLP, etc.)
 *
 * Each input JSON object is shaped:
 *   {
 *     "chapterN-study.html": { "title": "...", "sections": { id: htmlString, ... } },
 *     "chapterN-math.html":  { "title": "...", "sections": { id: htmlString, ... } }
 *   }
 *
 * The merged JS file output is:
 *   /* @generated by scripts/merge-translations.js * /
 *   window.COMPxxxx_SECTION_TRANSLATIONS = { ... };
 */

const fs = require("fs");
const path = require("path");

const courseDir = process.argv[2];
if (!courseDir) {
  console.error("Usage: node scripts/merge-translations.js <courseDir>");
  process.exit(1);
}

const repoRoot = path.resolve(__dirname, "..");
const courseRoot = path.join(repoRoot, courseDir);
const dataDir = path.join(courseRoot, "shared", "translations-data");
const outFile = path.join(courseRoot, "shared", "section-translations.js");

// derive variable name
let varName;
const m = courseDir.match(/^(\d{4})/);
if (m) {
  varName = "COMP" + m[1] + "_SECTION_TRANSLATIONS";
} else {
  varName = courseDir.toUpperCase().replace(/[^A-Z0-9]/g, "_") +
    "_SECTION_TRANSLATIONS";
}

if (!fs.existsSync(dataDir)) {
  console.error(`Data dir not found: ${dataDir}`);
  process.exit(1);
}

const merged = {};
const files = fs
  .readdirSync(dataDir)
  .filter((f) => f.endsWith(".json"))
  .sort((a, b) => {
    // chapter1 < chapter2 < ... < chapter10
    const an = parseInt(a.match(/chapter(\d+)/)?.[1] || "0", 10);
    const bn = parseInt(b.match(/chapter(\d+)/)?.[1] || "0", 10);
    return an - bn;
  });

if (files.length === 0) {
  console.error(`No JSON files found in ${dataDir}`);
  process.exit(1);
}

let pageCount = 0;
let sectionCount = 0;

for (const f of files) {
  const full = path.join(dataDir, f);
  let parsed;
  try {
    parsed = JSON.parse(fs.readFileSync(full, "utf8"));
  } catch (e) {
    console.error(`Failed to parse ${f}: ${e.message}`);
    process.exit(2);
  }
  for (const pageKey of Object.keys(parsed)) {
    if (!pageKey.endsWith(".html")) continue;
    if (merged[pageKey]) {
      console.warn(`WARN: duplicate page key ${pageKey} (already merged)`);
    }
    merged[pageKey] = parsed[pageKey];
    pageCount++;
    if (parsed[pageKey].sections) {
      sectionCount += Object.keys(parsed[pageKey].sections).length;
    }
  }
}

const banner = "/* @generated by scripts/merge-translations.js — do not edit by hand;\n" +
  " * source files live in <course>/shared/translations-data/*.json */\n";

const body =
  `window.${varName} = ` + JSON.stringify(merged) + ";\n";

fs.writeFileSync(outFile, banner + body, "utf8");

const sizeKb = (fs.statSync(outFile).size / 1024).toFixed(1);
console.log(
  `wrote ${path.relative(repoRoot, outFile)}  (${pageCount} pages, ${sectionCount} sections, ${sizeKb} KB)`
);
